package com.asoview.weather.core.model.city;

public class CitySummary {

    String id  = "";
    String cityName = "";

    public CitySummary() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getCityName() {
        return cityName;
    }

    public void setCityName(String cityName) {
        this.cityName = cityName;
    }
}
